# Escape Sequence Characters
# Sequence of Characters after backslash "\"
# Escape Sequence Characters comprEscape Sequence Charactersse of more that one characters but represent one character when used within the strring

a = "Hello my name is Mahesh\ni am a freelancer"  # for new line
b = "Hello my name is Mahesh\t am a freelancer" # for tab
c = "Hello my name is Mahesh  am a \'freelancer\'" # for singlequote
d = "Hello my name is Mahesh\\ am a freelancer" # for backslash

print(a)
print(b)
print(c)
print(d)